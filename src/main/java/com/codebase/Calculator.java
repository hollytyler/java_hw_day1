package com.codebase;

public class Calculator {
    public int addNumber(int a, int b){
        return (a + b);
    }

    public int subtractNumber(int a, int b){
        return (a - b);
    }

    public int multiplyNumber(int a, int b){
        return (a * b);
    }

    public double divideNumber(double a, double b){
        return (a / b);
    }
}
